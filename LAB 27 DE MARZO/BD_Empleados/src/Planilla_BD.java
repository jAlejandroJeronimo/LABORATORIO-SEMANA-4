    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Codigo que permite consultar registros en la base de datos
        try{
            Connection cn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3307/bd_ins", "root", "");
            PreparedStatement pst = (PreparedStatement) cn.prepareStatement("select * from empleado_6560 where codigo_empleado = ?");
            pst.setString(1, txt_buscar.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                txt_empleado.setText(rs.getString("nombre_empleado"));
                txt_codigopuesto.setText(rs.getString("codigo_puesto"));
                txt_codigodepto.setText(rs.getString("codigo_departamento"));
                txt_sueldo.setText(rs.getString("sueldo_empleado"));
                txt_status.setText(rs.getString("estatus_empleado"));
            } else {
                JOptionPane.showMessageDialog(null, "empleado no registrado.");
            }

        }catch (Exception e){

        }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Codigo que permite insertar registros en al base de datos
        try{
            Connection cn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3307/bd_ins", "root", "");
            PreparedStatement pst = (PreparedStatement) cn.prepareStatement("insert into empleado_6560 values(?,?,?,?,?)");

            pst.setString(1, "0");
            pst.setString(2, txt_empleado.getText().trim());
            pst.setString(3, txt_codigopuesto.getText().trim());
            pst.setString(4, txt_codigodepto.getText().trim());
            pst.setString(5, txt_sueldo.getText().trim());
            pst.setString(6, txt_status.getText().trim());
            pst.executeUpdate();

            txt_codigo.setText("");
            txt_empleado.setText("");
            txt_codigopuesto.setText("");
            txt_codigodepto.setText("");
            txt_sueldo.setText("");
            txt_status.setText("Registro exitoso.");
        }catch (Exception e){

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Codigo que permite actualizar registros en la base de datos
        try {
            String ID = txt_buscar.getText().trim();

            Connection cn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3307/bd_ins", "root", "");
            PreparedStatement pst = (PreparedStatement) cn.prepareStatement("update alumnos set NombreAlumno = ?, Grupo = ? where ID = " + ID);

            pst.setString(1, txt_codigo.getText().trim());
            pst.setString(2, txt_empleado.getText().trim());
            pst.executeUpdate();

            label_status.setText("Modificaci√≥n exitosa.");

        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton2ActionPerformed

